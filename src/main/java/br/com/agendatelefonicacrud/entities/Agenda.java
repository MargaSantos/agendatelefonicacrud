package br.com.agendatelefonicacrud.entities;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@Table(name = "agenda_telefonica")
public class Agenda {

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO, generator = "sequence_agenda")
	@SequenceGenerator(name = "sequence_agenda", sequenceName = "sequence_agenda", initialValue = 2, allocationSize = 1)
	@Column(name = "id_agenda", nullable = false)
	private Long id;
	@Column(name = "nome", nullable = false)
	private String nome;
	@Column(name = "email", nullable = false)
	private String email;
	@Column(name = "telefone", nullable = false)
	private String telefone;

	public Agenda() {
		super();
	}

	public Agenda(Long id, String nome, String email, String telefone) {
		super();
		this.id = id;
		this.nome = nome;
		this.email = email;
		this.telefone = telefone;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getTelefone() {
		return telefone;
	}

	public void setTelefone(String telefone) {
		this.telefone = telefone;
	}

}
